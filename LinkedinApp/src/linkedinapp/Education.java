package linkedinapp;

public class Education {
	String title;
	String degreeName;
	String dateRange;
	String description;
	
	public Education(String title, String degreeName, String dateRange, String description){
		this.title = title;
		this.degreeName = degreeName;
		this.dateRange = dateRange;
		this.description = description;
	}
}
